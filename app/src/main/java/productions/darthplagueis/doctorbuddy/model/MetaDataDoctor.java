package productions.darthplagueis.doctorbuddy.model;


public class MetaDataDoctor {

    private String data_type;
    private String item_type;
    private int total;
    private int count;
    private int skip;
    private int limit;

    public String getData_type() {
        return data_type;
    }

    public String getItem_type() {
        return item_type;
    }

    public int getTotal() {
        return total;
    }

    public int getCount() {
        return count;
    }

    public int getSkip() {
        return skip;
    }

    public int getLimit() {
        return limit;
    }
}
